export class Employee {

  id: number; 
  username: string;
  firstname: string;
  lastname: string;
  gender: string;

  constructor(id: number, username: string, firstname: string, lastname: string, gender: string ) {
    this.id = id;
    this.username = username;
    this.firstname = firstname;
    this.lastname = lastname;
    this.gender = gender;
  }



}
